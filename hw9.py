# Вводим количество элементов в массиве
N = int(input("Введите количество элементов в массиве: "))

# Вводим элементы массива
A = []
for i in range(N):
    A.append(int(input(f"Введите элемент A[{i+1}]: ")))

# Вводим число X, к которому ищем ближайший элемент
X = int(input("Введите число X: "))

# Инициализируем переменные для хранения ближайшего элемента и его разницы с X
nearest = A[0]
min_diff = abs(X - A[0])

# Ищем ближайший элемент в массиве A
for num in A:
    diff = abs(X - num)
    if diff < min_diff:
        nearest = num
        min_diff = diff

# Выводим результат
print(f"Ближайший элемент к числу {X} в массиве A: {nearest}")
