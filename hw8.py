# Вводим количество элементов в массиве
N = int(input("Введите количество элементов в массиве: "))

# Вводим элементы массива
A = []
for i in range(N):
    A.append(int(input(f"Введите элемент A[{i+1}]: ")))

# Вводим число X, которое нужно найти
X = int(input("Введите число X: "))

# Считаем количество вхождений числа X в массив A
count = 0
for num in A:
    if num == X:
        count += 1

# Выводим результат
print(f"Число {X} встречается {count} раз(а) в массиве A.")
