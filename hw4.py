# Ввод размеров шоколадки и количества долек, которые нужно отломить
n, m, k = map(int, input("Введите размеры шоколадки и количество долек для отломки (через пробел): ").split())

# Проверка на корректность ввода
if n <= 0 or m <= 0 or k <= 0:
    print("Ошибка: введите положительные значения!")
elif k > n * m:
    print("Ошибка: количество долек для отломки больше, чем количество долек в шоколадке!")
else:
    # Проверка на возможность отломить k долек
    if k % n == 0 or k % m == 0:
        print("Да, можно отломить {} долек.".format(k))
    else:
        print("Нет, нельзя отломить {} долек.".format(k))
